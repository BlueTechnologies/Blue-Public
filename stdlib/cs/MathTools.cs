class MathTools {

dynamic arcsine(dynamic x) {
    return System.Math.asin(x);
}

dynamic arccos(dynamic x) {
    return System.Math.acos(x);
}

dynamic cosine(dynamic x) {
    return System.Math.cos(x);
}

dynamic power(dynamic x, dynamic y) {
    return System.Math.pow(x, y);
}

dynamic sine(dynamic x) {
    return System.Math.sin(x);
}

dynamic floorValue(dynamic x) {
    return System.Math.floor(x);
}

}