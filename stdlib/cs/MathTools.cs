class MathTools {

dynamic arcsine(dynamic x, dynamic y) {
    return System.Math.asin(x, y);
}

dynamic arccos(dynamic x, dynamic y) {
    return System.Math.acos(x, y);
}

dynamic cosine(dynamic x, dynamic y) {
    return System.Math.cos(x, y);
}

dynamic power(dynamic x, dynamic y) {
    return System.Math.pow(x, y);
}

dynamic sine(dynamic x, dynamic y) {
    return System.Math.sin(x, y);
}

dynamic floorValues(dynamic x, dynamic y) {
    return System.Math.floor(x, y);
}

}