class MathTools {

    static arcsine(x) {
        return Math.asin(x);
    }
    
    static arccos(x) {
        return Math.acos(x);
    }
    
    static cosine(x) {
        return Math.cos(x);
    }
    
    static power(x, y) {
        return Math.pow(x, y);
    }
    
    static sine(x) {
        return Math.sin(x);
    }
    
    static floorValues(x) {
        return Math.floor(x);
    }
    
}