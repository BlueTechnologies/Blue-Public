import java.lang.Math;

def arcsine(x, y) {
	return Math.asin(x, y);
}
	
def arccos(x, y) {
	return Math.acos(x, y);
}
	
def cosine(x, y) {
	return Math.cos(x, y);
}
	
def power(x, y) {
	return Math.pow(x, y);
}
	
def sine(x, y) {
	return Math.sin(x, y);
}
	
def floorValues(x, y) {
	return Math.floor(x, y);
}