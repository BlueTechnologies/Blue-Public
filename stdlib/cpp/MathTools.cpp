#include <math.h>

static double arcsine(double x) {
    return asin(x);
}

static double arccos(double x) {
    return acos(x);
}

static double cosine(double x) {
    return cos(x);
}

static double power(double x, double y) {
    return pow(x, y);
}

static double sine(double x) {
    return sin(x);
}

static double floorValue(double x) {
    return floor(x);
}