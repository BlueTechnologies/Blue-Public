import Base

global function arcsine(x) {
    return Base.Math.asin(x);
}

global function arccos(x) {
    return Base.Math.acos(x);
}

global function cosine(x) {
    return Base.Math.cos(x);
}

global function power(x, y) {
    return Base.Math.pow(x, y);
}

global function sine(x) {
    return Base.Math.sin(x);
}

global function floorValues(x) {
    return Base.Math.floor(x);
}